# Using the Keyword Argument end=""
#As you can see, the end keyword argument
# determines the characters the print() function sends to
# the output once it reaches the end of its positional arguments.
#The default behavior reflects the situation where the end keyword 
# argument is implicitly used in the following way: end="\n".
print("My name is", end="")
print("Jacques")

print()
